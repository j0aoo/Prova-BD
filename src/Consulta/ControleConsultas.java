    private void ComboNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ComboNomeActionPerformed
        // TODO add your handling code here:

        Connection con = Conexao.AbrirConexao();
        ConsultaDAO sql = new ConsultaDAO(con);
        List<Consulta> lista = new ArrayList<>();

        lista = sql.ConsultaInformConsulta(ComboNome.getSelectedItem().toString());

        for (Consulta b : lista) {

            jTf_Codigo.setText(""+b.getCodigo());
            dataMarcada.setText("" + b.getDataConsulta());
            horario.setText("" + b.getHorario());

        }

        Conexao.FecharConexao(con);
    }//GEN-LAST:event_ComboNomeActionPerformed

