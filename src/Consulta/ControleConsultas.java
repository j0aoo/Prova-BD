    public void AtualizaTable() {
        
        Connection con = Conexao.AbrirConexao();
        ConsultaDAO bd = new ConsultaDAO(con);
        List<Consulta> lista = new ArrayList<>();
        lista = bd.ListarConsulta();
        
        DefaultTableModel tbm = (DefaultTableModel) jTable.getModel();
        
        while (tbm.getRowCount() > 0) {
            
            tbm.removeRow(0);
            
        }

        int i = 0;
        for (Consulta tab : lista) {
            
            tbm.addRow(new String[i]);
            jTable.setValueAt(tab.getCod_cliente(), i, 0);
            jTable.setValueAt(tab.getNomeCli(), i, 1);
            jTable.setValueAt(tab.getDataConsulta(), i, 2);
            jTable.setValueAt(tab.getHorario(), i, 3);
            
            i++;
            
        }
        
        Conexao.FecharConexao(con);
        
    }
    
